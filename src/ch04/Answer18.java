package ch04;

public class Answer18 {

	public static void main(String[] args) {
		// 역계단식 별 찍기
		
		int i = 1;
		int j = 5;
		
		while(i<=5) {
			while(j>=i) {
				System.out.print("☆");
				j--;
			}
			System.out.println("");
			i++;
			j=5;
		}
				
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
